# Compara múltiples cadenas con if/elif/else

nombre = input("¿Cuál es tu nombre? ")

if nombre == "Harry":
    print("Gryffindor")
elif nombre == "Hermione":
    print("Gryffindor")
elif nombre == "Ron":
    print("Gryffindor")
elif nombre == "Draco":
    print("Slytherin")
else:
    print("¿Quién?")